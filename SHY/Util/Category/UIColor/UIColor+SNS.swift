import Foundation
import UIKit

extension UIColor {
    class func grayTwo() -> (UIColor) {
        return UIColor.colorWithHexString("#454545")
    }
    
    class func grayThree() -> (UIColor) {
        return UIColor.colorWithHexString("#929292")
    }
}
